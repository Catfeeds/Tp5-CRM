     * @param  \think\Request  $request
     * @return \think\Response
     */
    public function save(Request $request)
    {
        if ($request->isPost()){
            $rbacObj=new Rbac();
            $id=input('post.id');
            $role_id=input('post.role_id');
            if ($rbacObj->assignUserRole($id,[$role_id])){
                return true;
            }else{
                return false;
            }
        }
    }

